<li>If your language is supported by Google Translator, you would see that most of the labels have been translated and there is a note as "Translated by Google Translator."</li>
<li>For Google Translated labels,
    <ul>    
    <li>If you think the value is correct, simply leave it.</li>
    <li>If you think the values is wrong, edit the label and enter correct value.</li>
    <li>In both cases, remove the note "Translated by Google Translator." and add a "Reviewed by" note with your name (Ex: "Reviewed by Kayla Abbey).</li>
    </ul>
</li>
<li>There can be more than one translator for a language. In such cases, don't overwrite the notes added by another translator. However if you see that a label needs to be improved, edit the lable and explain your change in notes.</li>
<li>If a Target Label is empty, you would see whole row highlighted.</li>
<li>You should not translate variables (strings within percentage signs like %amount%) and the term OrangeHRM inside labels. However you may change the place they appear in Target label.</li>
<li>You need to review/translate all labels in all Language Groups.</li>
<li>It's advisable that you save the changes frequently.</li>
<li>Once you complete the translation, reply to your Localizit welcome email and inform the completion.</li>
</ul>

<a name="translatingEmails"></a>
<h2>How to Translate Emails</h2>

<ul>
<li>Currently only Leave module emails support language translation.</li>
<li><a href="../en_US.zip">Download the zip file</a> containing email templates.</li>
<li>You need to translate each template.</li> 
<li>When translating leave out variables and the term OrangeHRM similar to labels.</li>
<li>Once you finished the translation create a zip file mentioning the locale in name (like es_ES.zip) and email to OrangeHRM by replying to Localizit welcome email.</li>
</ul>

