<a name="translating"></a>
<h2>How to Translate Labels</h2>

<ul>
<li>Click on Translate Text tab.</li>
<li>Choose the Target Language, Language Group and then click on Display button.</li>
<li>You would see following four columns.</li>

<ul>
<li><strong>Source Label</strong> - Label in English. Not editable.</li>
<li><strong>Source Note</strong> - Notes explaining the source label. Ex: A country name. Not editable.</li>
<li><strong>Target Label</strong> - Label in your language. Editable.</li>
<li><strong>Target Note</strong> - Notes explaining the source label.</li>
</ul>

<li>Click on Edit button to start translating. </li>

